class BinarySplitTree:
    def __init__(self):
        self.isTree = False
        self.entropy = None
        self.mostLabel = None
        self.label = None
        self.trueChildren = None
        self.falseChildren = None
        self.attribute = None
        self.value = None
        self.isLeaf = False




class MultiSplitTree:
    def __init__(self):
        self.isTree = False
        self.entropy = None
        self.children = []
        self.isLeaf = False
        self.label = None
        self.mostLabel = None
        self.attribute = None
        self.value = None






